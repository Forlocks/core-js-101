/**
 * Sorts digit names
 *
 * @param {array} arr
 * @return {array}
 *
 * @example
 *   [] => []
 *   [ 'nine','one' ]                 => [ 'one', 'nine' ]
 *   [ 'one','two','three' ]          => [ 'one','two', 'three' ]
 *   [ 'nine','eight','nine','eight'] => [ 'eight','eight','nine','nine']
 *   [ 'one','one','one','zero' ]     => [ 'zero','one','one','one' ]
 */
function sortDigitNamesByNumericOrder(arr) {
  let result = [];
  let sort = [];

  arr.map(function(element) {
    switch (element) {
      case 'zero':
        result.push(0);
        break;
      case 'one':
        result.push(1);
        break;
      case 'two':
        result.push(2);
        break;
      case 'three':
        result.push(3);
        break;
      case 'four':
        result.push(4);
        break;
      case 'five':
        result.push(5);
        break;
      case 'six':
        result.push(6);
        break;
      case 'seven':
        result.push(7);
        break;
      case 'eight':
        result.push(8);
        break;
      case 'nine':
        result.push(9);
        break;
    }
  });

  result.sort();

  result.map(function(element) {
    switch (element) {
      case 0:
        sort.push('zero');
        break;
      case 1:
        sort.push('one');
        break;
      case 2:
        sort.push('two');
        break;
      case 3:
        sort.push('three');
        break;
      case 4:
        sort.push('four');
        break;
      case 5:
        sort.push('five');
        break;
      case 6:
        sort.push('six');
        break;
      case 7:
        sort.push('seven');
        break;
      case 8:
        sort.push('eight');
        break;
      case 9:
        sort.push('nine');
        break;
    }
  });

  return sort;
}

